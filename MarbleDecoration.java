	private int findResult(int a, int b)
	{
		if(a == b)
			return 2 *a;
		else
			return 2 * Math.min(a, b) + 1;
		
	}
	public int maxLength(int R, int G, int B)
	{
		if(R < G && R < B){
			return findResult(G, B);
		}else if(G < B){
			return findResult(B, R);
		}else{
			return findResult(G, R);
		}
	}
	
