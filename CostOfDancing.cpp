class CostOfDancing {
public:
	int minimum(int, vector <int>);
};

int CostOfDancing::minimum(int K, vector <int> danceCost) {
	
	int cost = 0;

	sort(danceCost.begin(), danceCost.end());	
	for (int i = 0; i < K; ++i)
	{
		cost += danceCost[i];
	}
	return cost;
}


