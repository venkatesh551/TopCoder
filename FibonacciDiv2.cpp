class FibonacciDiv2 {
public:
	int find(int);
	void gen_seq();
private:
	int fib_seq[100];
	int len;
	static const int max_n = 1000001;
};

void FibonacciDiv2::gen_seq()
{
	int i ;
	fib_seq[0] = 0;
	fib_seq[1] = 1;
	
	for (i = 2; fib_seq[i-1] <= max_n; i++)
	{
		fib_seq[i] = fib_seq[i-1] + fib_seq[i-2];
	}
	len = i;
}

int FibonacciDiv2::find(int N) {
	
	gen_seq();
	
	int l = 0, h = len-1;
	
	while (l <= h)
	{
		int m = (l + h)/2;
		
		if (fib_seq[m] == N)
		{
			return 0;
		}
		else if (fib_seq[m] < N)
		{
			l = m+1;
		}
		else
		{
			h = m-1;
		}
	}
	return min(N-fib_seq[h], fib_seq[l]-N);
}


