	public int minDucks(int[] duckTypes)
	{
		int[] duckCount = new int[51];
		int i, max, types;
		for(i = 0; i < duckTypes.length; i++){
			duckCount[duckTypes[i]]++;
		}
		max = types = 0;
		for(i = 1; i < 51; i++){
			if(duckCount[i] > 0){
				types++;
				if(max < duckCount[i])
					max = duckCount[i];
			}
		}
		return max*types;
	}
	
