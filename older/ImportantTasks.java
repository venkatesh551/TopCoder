	public int maximalCost(int[] complexity, int[] computers)
	{
		Arrays.sort(complexity);
		Arrays.sort(computers);
		
		int cnt = 0;
		for(int i = 0, j = 0; i < complexity.length && j < computers.length; j++)
		{
			if(complexity[i] <= computers[j])
			{
				cnt++;
				i++;
			}
		}
		return cnt;
	}
	
