	public int[] getUnsorted(int[] s)
	{
		int i;
		Arrays.sort(s);
		for(i = s.length-1; i > 0; i--)
		{
			if(s[i] > s[i-1])
			{	
				int temp = s[i];
				s[i] = s[i-1];
				s[i-1] = temp;
				break;
			
			}
		
		}
		if(i == 0){
			return new int[0];
		}else
			return s;
		
	}
	
