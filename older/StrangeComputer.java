	public int setMemory(String mem)
	{
		int ans = 0 ;
		char prev = '0';
		
		for(int i = 0; i < mem.length(); i++){
			if(mem.charAt(i) != prev){
				prev = mem.charAt(i);
				ans++;
			}
		}
		
		return ans;
		
	}
	
