	public int getMinimumSteps(int[] pos)
	{
		int ans = 0;
		
		for(int i = 0; i < pos.length && ans < 3; i++)
		{
				int temp = 0;
				
				switch(pos[i])
				{
					case 1: 
					case 3:
					case 4: temp = 1; break;
					case 2:
					case 5:
					case 7: temp = 2; break;
					case 6: temp = 3; break;
				}
				if(ans < temp)
					ans = temp;
		}
		return ans;
	}
	
