	public int getscore(String s)
	{
	
		int c = 1, n = s.length(), ans = n;
		for (int i = 1; i < n; ++i)
		{
			if (s.charAt(i) == s.charAt(i-1))
			{
				++c;
			}
			else
			{
				ans += c * (c-1)/2;
				c = 1;
			}
		}
		ans += c * (c-1)/2;
		return ans;
	}
	
