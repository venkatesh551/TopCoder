	public int[] find(int T, int[] requiredTime)
	{
		int[] result =  new int[2];
		int i;
		Arrays.sort(requiredTime);
		for(i = 1; i < requiredTime.length; i++)
			requiredTime[i] += requiredTime[i-1];
		for(i = 0; i < requiredTime.length && requiredTime[i] <= T; i++){
			result[0]++;
			result[1] += requiredTime[i];
		}	
		return result;
	}
	
