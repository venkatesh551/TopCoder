class PeacefulLine {
public:
	string makeLine(vector <int>);
};

string PeacefulLine::makeLine(vector <int> x) {
	map<int, int> m;
	int n = x.size();
	for (int i = 0; i < n; ++i)
	{
		++m[x[i]];
	}
	int max_cnt = 0;
	for (map<int, int>::iterator it = m.begin(); it != m.end(); ++it)
	{
		max_cnt = max(max_cnt, it->second);
	}
	int half = (n+1)/2;
	if (max_cnt > half)
	{
		return "impossible";
	}
	else
	{
		return "possible";
	}
}


