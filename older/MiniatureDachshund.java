	public int maxMikan(int[] mikan, int weight)
	{
		int i, len = mikan.length;
		
		Arrays.sort(mikan);		
		for (i = 0; i < len; i++)
		{
			weight += mikan[i];
			if (weight > 5000)
			{
				return i;
			}
		}
		return i;
	}
	
