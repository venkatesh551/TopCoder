	public int getMax(String places)
	{
		int C = 0, V = 0;
		int n = places.length();
		for(int i = 0; i < n; i++)
		{
			if(places.charAt(i) == 'V')
				V++;
			else
				C++;
		
		}
		return Math.min(n, 2*Math.min(C, V)+1);
	}
	
