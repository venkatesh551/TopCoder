class RectangleCoveringEasy {
public:
	int solve(int, int, int, int);
};

int RectangleCoveringEasy::solve(int a, int b, int c, int d) {
	
	if (a > b)
	{
		swap(a, b);
	}
	if (c > d)
	{
		swap(c, d);
	}
	if (a == c && b == d)
	{
		return -1;
	}
	else if (c >= a && d >= b)
	{
		return 1;
	}
	else
	{
		return -1;
	}
}


