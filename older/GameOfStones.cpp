class GameOfStones {
public:
	int count(vector <int>);
};

int GameOfStones::count(vector <int> stones) {
	
	int sum = 0, n  = stones.size();
	
	for (int i = 0; i < n; ++i)
	{
		sum += stones[i];
	}
	if (sum % n != 0)
	{
		return -1;
	}
	sort(stones.begin(), stones.end());
	int x = sum/n;
	int ans = 0;
	for (int i = 0; i < n; ++i)
	{
		if (stones[i] > x)
		{
			int d = stones[i] - x;
			if ((d & 1) == 1)
			{
				ans = -1; break;
			}
			else
			{
				ans += d/2;
			}
		}
	}
	return ans;

	
}


