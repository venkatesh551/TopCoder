	public int count(int[] rating)
	{
		int cnt = 0;
		
		char cur, prev = 'c';
		
		for(int i = 0; i < rating.length; ++i)
		{
			if (rating[i] < 1200)
			{
				cur = 'c';
			}
			else
			{
				cur = 'b';
			}
			if (prev != cur)
			{
				cnt++;
			}
			prev = cur;
			
		}
		return cnt;
	}
	
