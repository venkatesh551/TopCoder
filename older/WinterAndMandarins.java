	public int getNumber(int[] A, int k)
	{
		int min = 1000000007;
		int n = A.length;
		
		Arrays.sort(A);
		for (int  i = 0; i < n-k+1; ++i)
		{
			int d = A[i+k-1] - A[i];
			if (d < min)
			{
				min =  d;
			}
		}
		return min;
	}
	
