class CartInSupermarketEasy {
public:
	int calc(int, int);
};

int CartInSupermarketEasy::calc(int N, int K) {
	
	int cnt = 0, m = N;
	while (N > 1 && K > 0)
	{
		N = N/2;
		m = (N+1)/2;
		++cnt;
		K--;
	}
	return cnt + m;
}


