	public int find(int[] sequence)
	{	
	
		//In java all elements are initialised to 0 by default
		int[] arr = new int[48];
		int i, n = sequence.length;
		int res ; 
		if(n == 2)
			return 1;
		else
			res = 1 + n * (n-1)/2;
		for(i = 0; i < n; i++){
			arr[sequence[i]]++;		
		}
		for(i= 1; i < 48 ; i++){
			if(arr[i] > 1){
				res = res - (arr[i] * (arr[i] - 1))/2;
			}
		}
		return res;
		
	}
	
