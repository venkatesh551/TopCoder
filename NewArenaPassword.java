	public int minChange(String oldPassword, int K)
	{
		int cnt  = 0;
		int len = oldPassword.length();
		for(int  i = 0, j = len - K; i < K && j < len ; i++, j++){
			if(oldPassword.charAt(i) != oldPassword.charAt(j)){
				cnt++;
			}
		}
		if(len == 1)
			return 0;
		if(len-K+ 1 == K)
			return cnt - 1;
		else
			return cnt;
	}
	
