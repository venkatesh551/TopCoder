	private boolean isValidPair(String a, String b)
	{
		int alen  = a.length();
		int blen =  b.length();
		
		if (alen != blen  || alen == 1 || blen == 1)
		{
			return false;
		}
		for (int i = 1; i < alen; ++i)
		{
			String x = a.substring(0, i);
			String y = a.substring(i, alen);
			String z = y + x;
			if (z.equals(b))
				return true;
		}
		return false;
	}
	
	public int howManyPairs(String[] words)
	{
		int n = words.length;
		int ans = 0;
		
		for (int i = 0; i < n; ++i)
		{
			for (int j = i+1; j < n ; ++j)
			{
				if (isValidPair(words[i], words[j]))
				{
					ans++;
				}
			}
		
		}
		return ans;
	}
	
