	public int getNumber(String S)
	{
		int ans = 1, len = S.length();
		
		for (int i = 1; i < len; i++)
		{
			if (S.charAt(i) != S.charAt(i-1))
			{
				ans++;
			}
		
		}
		return ans;
	}
	
