	public int minCost(int[] altitude)
	{
		int cost = 0;
		
		for(int i = 0; i < altitude.length - 1; i++){
			if(altitude[i]  < altitude[i+1]){
				cost += altitude[i+1] - altitude[i];
				altitude[i+1] = altitude[i];
			}
		}
		return cost;
		
	}
	
