class DecipherabilityEasy {
public:
	string check(string, string);
};

string DecipherabilityEasy::check(string s, string t) {
	
	if (s.length() != t.length() +1)
	{
		return "Impossible";
	}
	int cnt = 0;
	for (int i = 0, j = 0; i< s.length() && j < t.length(); ++i)
	{
		if (s[i] == t[j])
		{
			++j;
		}
		else
		{
			++cnt;
		}
	}
	if (cnt <= 1)
	{
		return "Possible";
	}
	else
	{
		return "Impossible";
	}
}


