class RunningAroundPark {
public:
	int numberOfLap(int, vector <int>);
};

int RunningAroundPark::numberOfLap(int N, vector <int> d) {

	int ans = 1, len = d.size();
	
	for (int i = 1; i < len; ++i)
	{
		if (d[i] <= d[i-1])
		{
			ans++;
		}
	}
	return ans;
	
}


