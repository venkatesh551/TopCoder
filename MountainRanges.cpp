class MountainRanges {
public:
	int countPeaks(vector <int>);
};

int MountainRanges::countPeaks(vector <int> h) {

	int n = h.size();
	
	if (n == 1)
	{
		return 1;
	}
	int ans = 0;
	if (h[0] > h[1])
	{
		ans = 1;
	}
	for (int i = 1; i < n-1; ++i)
	{
		if (h[i] > h[i+1] && h[i] > h[i-1])
		{
			++i; ++ans;
		}
	}
	if (h[n-1] > h[n-2])
	{
		++ans;
	}
	return ans;
}


