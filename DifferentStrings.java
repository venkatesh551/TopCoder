	public int minimize(String A, String B)
	{
		
		int min = 1000;
		for(int i = 0; i <= (B.length() - A.length()) ; i++)
		{
			int diff = 0;
			for(int j = 0; j < A.length(); j++)
			{
				if(A.charAt(j) != B.charAt(i+j))
					diff++;
			}
			min = Math.min(min, diff);
		
		}
		return min;
	}
	
