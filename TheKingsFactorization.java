	public long[] getVector(long n, long[] primes)
	{
		int len = primes.length;
		long [] result = new long[2*len+2];
		int j = 0;
		for (int i = 0; i < len && n > 1; ++i)
		{
			if (!(i > 0 && primes[i] == primes[i-1]))
			{
				while (n % primes[i] == 0)	
				{
					n /= primes[i];
					result[j++] = primes[i]; 
				}
			}
		}
		long []ans = new long[j];
		for (int i = 0; i < j; ++i)
		{
			ans[i] = result[i];
		}
		Arrays.sort(ans);
		return ans;
	}
	
