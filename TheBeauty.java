	public int find(int n)
	{
		int[] digits = new int[10];
		int ans = 0;
		
		while(n > 0)
		{
			digits[n%10]++;
			n /= 10;
		}
		for(int i = 0; i < 10; i++)
		{
			if(digits[i] > 0)
				ans++;
		}
		return ans;
	}
	
