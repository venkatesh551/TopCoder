	private boolean isVowel(char c)
	{
	
		return  (c == 'a' || c == 'e' || c == 'i' || c == 'o' || c == 'u' || c == 'y') ;
	}
	public int getLength(String nickname)
	{
		int ans = nickname.length();
		int len = ans ;
		char prev = nickname.charAt(0);
		for(int i = 1; i < len; i++){
			char cur = nickname.charAt(i);
			if(!isVowel(prev) || !isVowel(cur))
			{
				prev = cur;
			}
			else
			{
				ans--;
			}
		}
		return ans;
	}
	
