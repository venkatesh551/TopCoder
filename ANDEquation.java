	public int restoreY(int[] A)
	{
		for(int i = 0; i < A.length; i++)	
		{
			int temp = (1 << 31) - 1;
			for(int j = 0; j < A.length ; j++)
			{
				if(i != j)
				{
					temp &= A[j];
				}
			
			}
			if(temp  == A[i])
				return temp;
		}
		return -1;		
	}
	
