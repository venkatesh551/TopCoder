	public long calculateSpace(int[] sizes, int clusterSize)
	{
		long ans = 0;
		for(int i = 0; i < sizes.length; i++)
		{
			ans += (sizes[i] / clusterSize) * clusterSize;
			if(sizes[i] % clusterSize > 0)
				ans += clusterSize;
		}
		return ans;
	}
	
