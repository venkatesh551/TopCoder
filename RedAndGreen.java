	public int minPaints(String row)
	{
		int i, j, k;
		int rCnt =0, gCnt = 0;
		
		for(i = 0; i < row.length() && row.charAt(i) == 'R'; i++)
				;
		for(j = row.length()-1; j >= 0 && row.charAt(j) == 'G'; j--)
				;
		for(k = i; k <= j; k++){
			if(row.charAt(k) == 'R')
				rCnt++;
			else
				gCnt++;
		}
		return Math.min(rCnt, gCnt);
	}
	
