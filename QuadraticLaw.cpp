	long long p0 = 1LL;
	QuadraticLaw * obj = new QuadraticLaw();
	clock_t start = clock();
	long long my_answer = obj->getTime(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
