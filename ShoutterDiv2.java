	public int count(int[] s, int[] t)
	{
		int cnt = 0;
		
		for(int i = 0; i < s.length; i++){
			for(int j = i+1; j < s.length; j++){
				if(t[i] <= t[j] && t[i] >= s[j])
					cnt++;
			}
		}
		return cnt;
		
	}
	
